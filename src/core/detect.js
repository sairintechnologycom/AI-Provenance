import { getExecOutput } from '@actions/exec';

export async function getGitOutput(repoPath, args) {
  try {
    const { exitCode, stdout, stderr } = await getExecOutput('git', args, {
      cwd: repoPath,
      silent: true,
      ignoreReturnCode: true
    });
    
    if (exitCode !== 0) {
      throw new Error(`Git command failed with exit code ${exitCode}. Stderr: ${stderr}`);
    }
    
    return stdout.trim();
  } catch (error) {
    throw new Error(`Git execution error: ${error.message}`);
  }
}

export async function analyzeCommit(repoPath, commitSha, trailerKey = 'AI-generated-by') {
  // 0. Check for shallow clone
  const isShallowStr = await getGitOutput(repoPath, ['rev-parse', '--is-shallow-repository']);
  if (isShallowStr === 'true') {
    return { 
      sha: commitSha,
      error: 'SHALLOW_CLONE',
      message: 'Shallow repository checkout detected. Analysis cannot walk history.'
    };
  }

  // 1. Fetch data via git
  const message = await getGitOutput(repoPath, ['show', '-s', '--format=%B', commitSha]);
  const diff = await getGitOutput(repoPath, ['show', commitSha]);
  const numstat = await getGitOutput(repoPath, ['show', '--numstat', '--format=', commitSha]);

  // 2. Analyze using the core data processor
  return analyzeCommitData({
    sha: commitSha,
    message,
    diff,
    files: numstat,
    trailerKey
  });
}

/**
 * Platform-agnostic commit analyzer
 * @param {Object} data 
 * @param {string} data.sha
 * @param {string} data.message
 * @param {string} data.diff
 * @param {string|Array} data.files - Either numstat string or array of file objects {additions, deletions}
 * @param {string} data.trailerKey
 */
export function analyzeCommitData({ sha, message, diff, files, trailerKey = 'AI-generated-by' }) {
  let aiTool = null;
  let confidence = 0;
  const methods = [];
  
  // 1. Check commit message for trailer
  const trailerRegex = new RegExp(`^${trailerKey}:\\s*(.+)$`, 'im');
  const trailerMatch = message.match(trailerRegex);
  
  if (trailerMatch) {
    aiTool = trailerMatch[1].trim() || 'Unknown AI';
    confidence = 100;
    methods.push('trailer');
  } else {
    // 2. Heuristic check on diff
    const heuristicRegex = /(AI[\s-]?generated|Generated by Copilot|Written by AI|auto-generated by AI)/i;
    
    // Check diff content if provided
    if (diff) {
      const heuristicMatch = diff.match(heuristicRegex);
      if (heuristicMatch) {
        aiTool = 'Unknown AI';
        confidence = 80;
        methods.push('heuristic');
      }
    }
  }
  
  // 3. Count lines and files
  let filesCount = 0;
  let linesAdded = 0;
  let linesRemoved = 0;

  if (Array.isArray(files)) {
    // Octokit files format
    filesCount = files.length;
    for (const file of files) {
      linesAdded += (file.additions || 0);
      linesRemoved += (file.deletions || 0);
    }
  } else if (typeof files === 'string') {
    // Numstat string format
    const statLines = files.split('\n').map(line => line.trim()).filter(line => line.length > 0);
    for (const line of statLines) {
      const parts = line.split(/\s+/);
      if (parts.length >= 3) {
        filesCount++;
        const added = parseInt(parts[0], 10);
        const removed = parseInt(parts[1], 10);
        if (!isNaN(added)) linesAdded += added;
        if (!isNaN(removed)) linesRemoved += removed;
      }
    }
  }
  
  return {
    sha,
    aiTool,
    confidence,
    files: filesCount,
    linesAdded,
    linesRemoved,
    methods
  };
}
